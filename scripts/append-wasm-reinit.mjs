#!/usr/bin/env node
/**
 * Append a `reinit(input)` export to a wasm-bindgen `--target web` glue file
 * (RB6 recovery).
 *
 * WHY THIS EXISTS
 * ---------------
 * wasm-bindgen's generated glue keeps its instantiated module in a MODULE-LEVEL
 * SINGLETON:
 *
 *     let wasmModule, wasm;
 *     async function __wbg_init(input) { if (wasm !== undefined) return wasm; ... }
 *     function   initSync(module)      { if (wasm !== undefined) return wasm; ... }
 *
 * So once a file traps, calling `init(...)` a second time SHORT-CIRCUITS and hands
 * back the SAME poisoned instance — its linear memory is still corrupt. RB6's
 * `WasmParserHost.ensureReady()` re-runs `init` after a trap, but against the raw
 * glue that recovery is a NO-OP: the poisoned instance is reused and the next file
 * runs on the same bad heap.
 *
 * `reinit(input)` nulls the singleton (and the cached memory views) FIRST, then
 * runs the normal init path — forcing a genuine `WebAssembly.instantiate`, a fresh
 * `WebAssembly.Instance`, and fresh linear memory. Proven empirically: a sentinel
 * byte written to the old instance's memory is GONE after `reinit` (the buffer is a
 * different object). `WasmParserHost` calls `reinit` (not `init`) on the recovery
 * path so a trapped parser truly recovers.
 *
 * WHY A POST-BUILD STEP (not a hand-edit of the glue)
 * ---------------------------------------------------
 * `pnpm --filter <pkg> wasm` runs `wasm-pack build --out-dir ../src/wasm`, which
 * OVERWRITES the glue on every build. A hand-edit would be silently clobbered.
 * This script is wired into each package's `wasm` npm script so the `reinit` export
 * is re-appended on every regeneration. It is idempotent (skips if already present)
 * and asserts the glue shape it depends on, failing loudly if a future wasm-bindgen
 * changes the singleton so we notice rather than ship a silent no-op recovery.
 *
 * The sibling `.d.ts` (also regenerated by wasm-pack, so also clobbered) gets a
 * matching `reinit` declaration so the workers can `import { reinit }` under
 * `tsc` without a `@ts-ignore`.
 *
 * Usage: node scripts/append-wasm-reinit.mjs <path-to-glue.js> [...more]
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';

const MARKER = '// --- RB6 reinit: forced fresh WebAssembly instance (appended by scripts/append-wasm-reinit.mjs) ---';

/** The `reinit` source appended to each glue. Reassigns the module-level `wasm` /
 *  `wasmModule` singleton (in scope because this is appended INTO the module) and
 *  nulls the cached memory views before delegating to the normal `__wbg_init`. */
const REINIT_SOURCE = `
${MARKER}
export async function reinit(module_or_path) {
    // Drop the singleton so __wbg_init below does a REAL instantiate instead of
    // returning the (poisoned) cached instance. Reassigning these module-scoped
    // bindings is why this must be appended INTO the glue module, not imported.
    wasm = undefined;
    wasmModule = undefined;
    // Stale views over the old (soon-freed) linear memory — null them so nothing
    // reads the discarded buffer if instantiation throws midway.
    cachedDataViewMemory0 = null;
    cachedUint8ArrayMemory0 = null;
    return __wbg_init(module_or_path);
}
`;

/** The `reinit` type appended to the sibling `.d.ts` (uses the `InitInput` /
 *  `InitOutput` types wasm-bindgen already declares there). */
const REINIT_DTS = `
${MARKER}
/**
 * RB6: force a FRESH WebAssembly instance (nulls wasm-bindgen's cached singleton
 * first, so a poisoned instance is genuinely replaced rather than reused).
 */
export function reinit (module_or_path: { module_or_path: InitInput | Promise<InitInput> } | InitInput | Promise<InitInput>): Promise<InitOutput>;
`;

/** Shape assertions: if a future wasm-bindgen changes these, `reinit` would be a
 *  silent no-op, so fail the build instead. */
const REQUIRED_TOKENS = [
  'let wasmModule, wasm;',
  'let cachedDataViewMemory0 = null;',
  'let cachedUint8ArrayMemory0 = null;',
  'if (wasm !== undefined) return wasm;',
  'async function __wbg_init(',
];

/** Keep the sibling `.d.ts` in sync so `import { reinit }` type-checks. Runs even
 *  when the `.js` already had `reinit` (e.g. a partial prior run), so the two
 *  files never drift. Returns false on an unexpected failure. */
function syncDts(jsPath) {
  const dtsPath = jsPath.replace(/\.js$/, '.d.ts');
  if (!existsSync(dtsPath)) return true; // some builds emit no .d.ts; not fatal
  const dts = readFileSync(dtsPath, 'utf8');
  if (dts.includes(MARKER)) return true;
  writeFileSync(dtsPath, dts.replace(/\s*$/, '\n') + REINIT_DTS, 'utf8');
  console.log(`append-wasm-reinit: appended reinit type to ${dtsPath}`);
  return true;
}

let failed = false;
for (const path of process.argv.slice(2)) {
  let src;
  try {
    src = readFileSync(path, 'utf8');
  } catch (err) {
    console.error(`append-wasm-reinit: cannot read ${path}: ${err.message}`);
    failed = true;
    continue;
  }

  if (src.includes(MARKER)) {
    console.log(`append-wasm-reinit: ${path} already has reinit, skipping .js`);
    syncDts(path); // still reconcile the .d.ts
    continue;
  }

  const missing = REQUIRED_TOKENS.filter((t) => !src.includes(t));
  if (missing.length > 0) {
    console.error(
      `append-wasm-reinit: ${path} is missing expected wasm-bindgen tokens, ` +
        `so appended reinit would not force a fresh instance:\n  - ` +
        missing.join('\n  - ') +
        `\nDid wasm-bindgen change its glue shape? Update this script.`,
    );
    failed = true;
    continue;
  }

  writeFileSync(path, src.replace(/\s*$/, '\n') + REINIT_SOURCE, 'utf8');
  console.log(`append-wasm-reinit: appended reinit to ${path}`);
  syncDts(path);
}

process.exit(failed ? 1 : 0);
